<p>Coalition Restaurant combines an inspired menu with a casually sophisticated setting in a historic downtown Excelsior main street building.  Coalition is the creation of Chef Eli Wollenzien and reflects his passion for cooking with a diverse and inspired menu.</p>
<p>Wollenzien has partnered with general manager and co-owner Deacon Eells and the two have a long, storied history growing up together in small town River Falls, WI, moving and immersing themselves in a variety of Twin Cities restaurants, and now returning to a close knit community for their first solo venture.</p>
<p>The menu takes its inspiration from classic American dishes like beef, lamb, pork and walleye, creatively prepared, vibrantly presented, and influenced by cuisines around the world reflecting the global, sophisticated, expanded palates of today. </p>
<p>The restaurant is located in a historic Excelsior brick building dating from 1886.  Creating a sense of the natural evolution of the building was a goal of the designer, Kara Karpenske of Kamarron Design, Inc. (KDI) of Edina.  New rustic floors, tall booths, oversized chandeliers, and industrial lighting enhance the look of the exposed original brick.  Antiqued mirrored panels set off the white marble design bar top and warm toned leather bar stools create an inviting space to enjoy Chef Eli&#8217;s crafted cocktail menu regionally sourced from such boutique distilleries as 45th Parallel Vodka (New Richmond, WI), North Shore Gin #11 (Lake Bluff, IL), Cedar Ridge Iowa Bourbon (Swisher, Iowa), and locally produced Bittercube Bitters (Minneapolis, MN). A large selection of wine and MN sourced tap beers complement the full bar.</p>
<p>Open for lunch and dinner seven days a week, Coalition will become a neighborhood fixture and a Lake Minnetonka destination for quality dining.</p>

